// --- RENDER METRICS CARDS ---
function renderMetrics() {
  const container = document.getElementById('metrics-container');
  const countBadge = document.getElementById('catalog-count-badge');
  const scrollContainer = document.getElementById('catalog-scroll-container');
  if (!container) return;

  let filtered = METRICS_DATA.filter(metric => {
    // Category filter
    if (currentCategory === 'saved') {
      if (!bookmarkedMetricIds.includes(metric.id)) return false;
    } else if (currentCategory !== 'all' && metric.category !== currentCategory) {
      return false;
    }

    // Search query filter
    if (currentSearchQuery) {
      const q = currentSearchQuery;
      const matchName = metric.name.toLowerCase().includes(q);
      const matchAcronym = metric.acronym.toLowerCase().includes(q);
      const matchMemory = metric.memoryTrick.toLowerCase().includes(q);
      const matchDesc = metric.whatIsIt.toLowerCase().includes(q);
      const matchWhy = metric.whyItMatters.toLowerCase().includes(q);

      return matchName || matchAcronym || matchMemory || matchDesc || matchWhy;
    }

    return true;
  });

  if (countBadge) {
    countBadge.innerText = `Showing ${filtered.length} of ${METRICS_DATA.length} Metrics`;
  }

  if (filtered.length === 0) {
    container.innerHTML = `
      <div style="grid-column: 1 / -1; text-align: center; padding: 40px; color: var(--text-muted);">
        <p style="font-size: 1.1rem; font-weight: 700; margin-bottom: 8px;">No metrics found matching "${currentSearchQuery}"</p>
        <p style="font-size: 0.88rem;">Try searching for keywords like "ROE", "P/E", "Debt", "Margin", "Cash Flow", or reset filters.</p>
      </div>
    `;
    return;
  }

  container.innerHTML = filtered.map(metric => createMetricCardHtml(metric)).join('');
  if (scrollContainer && currentSearchQuery) {
    scrollContainer.scrollTop = 0;
  }
}

