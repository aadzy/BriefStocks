function toggleChecklistItem(id) {
  checklistState[id] = !checklistState[id];
  localStorage.setItem('briefstocks_checklist', JSON.stringify(checklistState));
  renderChecklist();
  updateChecklistScore();
}

function resetChecklist() {
  checklistState = {};
  localStorage.setItem('briefstocks_checklist', JSON.stringify(checklistState));
  renderChecklist();
  updateChecklistScore();
}

function updateChecklistScore() {
  const count = Object.values(checklistState).filter(Boolean).length;
  const total = CHECKLIST_ITEMS.length;
  const pct = Math.round((count / total) * 100);

  const fill = document.getElementById('score-bar-fill');
  const scoreText = document.getElementById('checklist-score-text');
  const verdictPill = document.getElementById('verdict-pill');

  if (fill) fill.style.width = `${pct}%`;
  if (scoreText) scoreText.innerText = `${count} / ${total} Checks Passed (${pct}%)`;

  if (verdictPill) {
    if (count === 8) {
      verdictPill.innerText = "🟢 Excellent Candidate (Pass)";
      verdictPill.style.color = "var(--excellent-color)";
      verdictPill.style.background = "var(--excellent-bg)";
    } else if (count >= 6) {
      verdictPill.innerText = "🟢 Good Candidate (Investigate)";
      verdictPill.style.color = "var(--good-color)";
      verdictPill.style.background = "var(--good-bg)";
    } else if (count >= 4) {
      verdictPill.innerText = "🟡 Moderate (Deep Dive Needed)";
      verdictPill.style.color = "var(--average-color)";
      verdictPill.style.background = "var(--average-bg)";
    } else {
      verdictPill.innerText = "🔴 High Risk / Failed Checklist";
      verdictPill.style.color = "var(--redflag-color)";
      verdictPill.style.background = "var(--redflag-bg)";
    }
  }
}

