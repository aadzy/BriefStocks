// --- EVENT LISTENERS ---
function setupEventListeners() {
  const searchInput = document.getElementById('search-input');
  const catalogSearchInput = document.getElementById('catalog-search-input');

  if (searchInput) {
    searchInput.addEventListener('input', (e) => {
      currentSearchQuery = e.target.value.toLowerCase().trim();
      if (catalogSearchInput) catalogSearchInput.value = e.target.value;
      renderMetrics();
    });
  }

  if (catalogSearchInput) {
    catalogSearchInput.addEventListener('input', (e) => {
      currentSearchQuery = e.target.value.toLowerCase().trim();
      if (searchInput) searchInput.value = e.target.value;
      renderMetrics();
    });
  }

  // Keyboard shortcut Ctrl+K / '/' for search
  document.addEventListener('keydown', (e) => {
    if ((e.ctrlKey || e.metaKey) && e.key === 'k') {
      e.preventDefault();
      if (catalogSearchInput) catalogSearchInput.focus();
      else if (searchInput) searchInput.focus();
    } else if (e.key === '/' && document.activeElement !== searchInput && document.activeElement !== catalogSearchInput) {
      e.preventDefault();
      if (catalogSearchInput) catalogSearchInput.focus();
      else if (searchInput) searchInput.focus();
    }
  });

  // Screener Evaluator Form
  const evaluatorForm = document.getElementById('screener-eval-form');
  if (evaluatorForm) {
    evaluatorForm.addEventListener('submit', (e) => {
      e.preventDefault();
      runLiveScreenerEvaluation();
    });
  }
}

